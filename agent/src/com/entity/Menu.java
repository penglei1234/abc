package com.entity;

import java.util.Date;

public class Menu {
	private int menuId;
	private String menuName;
	private int menuFirstMenu;
	private int menuState;
	private String menuUrl;
	private String menuIntro;
	private String menuFlag;
	private int menuIdDel;
	private Date menuTime;
	public Date getMenuTime() {
		return menuTime;
	}
	public void setMenuTime(Date menuTime) {
		this.menuTime = menuTime;
	}
	public int getMenuId() {
		return menuId;
	}
	public void setMenuId(int menuId) {
		this.menuId = menuId;
	}
	public String getMenuName() {
		return menuName;
	}
	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
	public int getMenuFirstMenu() {
		return menuFirstMenu;
	}
	public void setMenuFirstMenu(int menuFirstMenu) {
		this.menuFirstMenu = menuFirstMenu;
	}
	public int getMenuState() {
		return menuState;
	}
	public void setMenuState(int menuState) {
		this.menuState = menuState;
	}
	public String getMenuUrl() {
		return menuUrl;
	}
	public void setMenuUrl(String menuUrl) {
		this.menuUrl = menuUrl;
	}
	public String getMenuIntro() {
		return menuIntro;
	}
	public void setMenuIntro(String menuIntro) {
		this.menuIntro = menuIntro;
	}
	public String getMenuFlag() {
		return menuFlag;
	}
	public void setMenuFlag(String menuFlag) {
		this.menuFlag = menuFlag;
	}
	public int getMenuIdDel() {
		return menuIdDel;
	}
	public void setMenuIdDel(int menuIdDel) {
		this.menuIdDel = menuIdDel;
	}
	

}
